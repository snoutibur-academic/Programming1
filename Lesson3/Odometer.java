public class Odometer {
	public static void main(String[] args) {
		for (int m = 0; m <10; m++) {
			for (int hks = 0; hks < 10; hks++) { 
				for (int tks = 0; tks < 10; tks++) {
					for (int ks = 0; ks < 10; ks++) {
						for (int hungiez = 0; hungiez < 10; hungiez++) {
							for (int tens = 0; tens < 10; tens++) {
								for (int ones = 0; ones < 10; ones++) {
									System.out.println(m + "|" + hks + "|" +tks + "|" + ks + "|" + hungiez + "|" + tens + "|" + ones);

								}
							}
						}
					}
				}
			}
		}
	}

}
